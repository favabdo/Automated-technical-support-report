def build_prompt(chat_history, existing_categories: list):
    cats_str = "\n".join(f"- {c}" for c in existing_categories) if existing_categories else "لا يوجد تصنيفات سابقة بعد"
    return f"""
You are a technical support assistant for monitoring the quality of solutions and the efficiency of the agent.
Overlook spelling mistakes. You are receiving a chat from the ChatWoot platform.

You MUST respond in EXACTLY this format — no extra text before or after:

النوع: 1
المشكلة:  وصف مختصر ودقيق للمشكلة وبدون تصنيف المشكله فقط ما تم عمله او تم انجازه او ما يدل علي ذلك
التصنيف: تصنيف المشكلة
الملخص: ملخص تفصيلي وشامل للمحادثة كاملة

Rules:
- النوع: اكتب "1" إذا تحقق أي مما يلي:
    * قال الأيجينت أنه تم عمل أي شيء أو اتخاذ أي إجراء فعلي
    * قال الأيجينت "تمام" أو ما يشبهها مما يدل على إنهاء المشكلة
    * رد العميل بما يدل على رضاه أو تأكيده أن المشكلة انتهت (مثل: "تمام، شكراً")
  اكتب "0" إذا تحقق أي مما يلي:
    * قال الأيجينت "تواصل معنا لاحقاً" أو "سيتم التواصل معك" أو "سيتم الرجوع للإدارة"
    * قال الأيجينت "شغالين على التحديث" أو ما يدل على أن المشكلة لا تزال معلقة
    * لم يحدث أي حل فعلي أو إجراء ملموس في المحادثة
-   المشكلة:  بدون تصنيف المشكله فقط ما تم عمله او تم انجازه او ما يدل علي ذلك وصف دقيق للمشكلة فقط (لا رسائل ترحيب، لا إسناد محادثة). كن موجزاً دون إغفال التفاصيل المهمه
- التصنيف: VERY IMPORTANT — راجع قائمة التصنيفات الموجودة أدناه. إذا كانت المشكلة تندرج تحت أي منها أو تشبهها، استخدم نفس التصنيف حرفياً. فقط إذا كانت مختلفة تماماً، اكتب تصنيفاً جديداً قصيراً.
- الملخص: اكتب ملخصاً تفصيلياً وشاملاً يتضمن: طبيعة المشكلة، الخطوات المتخذة، ردود الفعل، والنتيجة النهائية.
- اكتب دائماً بالعربية وتاكد انها عربيه سليمه مئه بالمئه ومش مسموح اي حرف واحد حتي يكون بلغه غير العربيه السليمه اوعي تنسي القاعده دي 
- لا تضف أي سطر إضافي خارج الأسطر الأربعة أعلاه.

التصنيفات الموجودة حالياً:
{cats_str}

Chat:
{chat_history}
"""
