def build_prompt(chat_history, existing_categories: list):
    cats_str = "\n".join(f"- {c}" for c in existing_categories) if existing_categories else "لا يوجد تصنيفات سابقة بعد"
    return f"""
You are a technical support assistant for monitoring the quality of solutions and the efficiency of the agent.
Overlook spelling mistakes. You are receiving a chat from the ChatWoot platform.

You MUST respond in EXACTLY this format — no extra text before or after:

النوع: 1
المشكلة: وصف مختصر ودقيق للمشكلة وبدون تصنيف المشكله فقط ما تم عمله او تم انجازه او ما يدل علي ذلك
التصنيف: تصنيف المشكلة
الملخص: ملخص تفصيلي وشامل للمحادثة كاملة ولكن بدون الرسائل الترحيبه او عمليات الاسناد الي ايجنت باختصار لا تلخص رسائل النظام او البوت فقط ما بين العميل والايجنت

Rules:
- النوع: اكتب "1" إذا تحقق أي مما يلي:
    * قال الأيجينت أنه تم عمل أي شيء أو اتخاذ أي إجراء فعلي
    * قال الأيجينت "تمام" أو ما يشبهها مما يدل على إنهاء المشكلة
    * رد العميل بما يدل على رضاه أو تأكيده أن المشكلة انتهت (مثل: "تمام، شكراً")
  اكتب "0" إذا تحقق أي مما يلي:
    * قال الأيجينت "تواصل معنا لاحقاً" أو "سيتم التواصل معك" أو "سيتم الرجوع للإدارة"
    * قال الأيجينت "شغالين على التحديث" أو ما يدل على أن المشكلة لا تزال معلقة
    * ذكر الأيجينت انقطاع في الخدمة أو مشكلة خارجية ولم يتخذ إجراءً فعلياً
    * لم يحدث أي حل فعلي أو إجراء ملموس في المحادثة لكن المشكلة واضحة ومحددة
  اكتب "2" إذا تحقق أي مما يلي:
    * كانت المحادثة مجرد استفسار عام ورد الأيجينت عليه دون أن يكون هناك مشكلة فعلية
    * تبين أن المتواصل اتصل بالخطأ أو أرسل لرقم خاطئ
    * تم تحويل العميل لقسم آخر (مثل المبيعات) وأُغلقت المحادثة دون مشكلة تقنية أو خدمية
    * لم يرد العميل بعد رد الأيجينت ولم تتضح أي مشكلة محددة
    * لم يكن هناك أي تواصل حقيقي يدل على مشكلة بعينها
- المشكلة: بدون تصنيف المشكله فقط ما تم عمله او تم انجازه او ما يدل علي ذلك وصف دقيق للمشكلة فقط (لا رسائل ترحيب، لا إسناد محادثة). كن موجزاً دون إغفال التفاصيل المهمه. إذا كان النوع "2" اكتب وصفاً مختصراً لطبيعة التواصل فقط.
- التصنيف: VERY IMPORTANT — راجع قائمة التصنيفات الموجودة أدناه. إذا كانت المشكلة تندرج تحت أي منها أو تشبهها، استخدم نفس التصنيف حرفياً. فقط إذا كانت مختلفة تماماً، اكتب تصنيفاً جديداً قصيراً. إذا كان النوع "2" اكتب "لم يتم تحديد مشكلة".
- الملخص: اكتب ملخصاً تفصيلياً وشاملاً يتضمن: طبيعة التواصل، الخطوات المتخذة إن وجدت، ردود الفعل، والنتيجة النهائية.
- VERY IMPORTANT — اكتب دائماً بالعربية الفصحى السليمة مئة بالمئة. ممنوع تماماً أي كلمة أو حرف بأي لغة أخرى غير العربية في أي حقل من الحقول الأربعة — بما في ذلك أسماء الأشخاص بالإنجليزية أو أي لغة أجنبية.
- لا تلخص رسائل النظام أو البوت أو رسائل الإسناد أو الترحيب في الملخص — فقط ما دار بين العميل والأيجينت فعلياً.
- لا تضف أي سطر إضافي خارج الأسطر الثلاثه أعلاه.

التصنيفات الموجودة حالياً:
{cats_str}

Chat:
{chat_history}
"""